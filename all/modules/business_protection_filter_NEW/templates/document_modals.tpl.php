 <div class="remodal" data-remodal-id="companyshareholderoptiona">
    <button data-remodal-action="close" class="remodal-close"></button>
    <h1 class="modal-title">Companies Act 2014</h1>
    <h5>In order for a company to buy back its own shares in a tax efficient manner it must meet the following conditions as set out in the Companies Act 2014:</h5>

    <div class="row center">
        <div class="twleve columns leftalign ">
            <p>The buy back must be allowed by either</p>
            <ul class="protectionList">
                <li>The company’s constitution</li>
                <li>The rights attaching to those shares, or</li>
                <li>A special resolution of the company</li>
            </ul>
            <p>A company cannot buy back all its own shares</p>
            <p>The company can only buy the shares back ‘out of profits available for distribution’</p>
            <p>The legal agreement must be authorised by a special resolution.</p>
            <p>Where the buy back of shares is to be authorised by a special resolution;</p>
            <ul class="protectionList">
                <li>The legal agreement must be made available to the members or at the company office in advance of the meeting</li>
                <li>Will be ineffective if a shareholder holding shares affected by the resolution exercises his / her voting rights in respect of the shares in favour of the resolution and it would not have been carried without his / her vote; and</li>
                <li>The terms of the agreement can only be amended or changed by a further special resolution</li>
            </ul>
        </div>
    </div>
</div>


