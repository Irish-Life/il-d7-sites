                <p>The authorisation for the company to enter each Agreement may need to be approved by a special resolution of the company.</p>
                <p>The Companies Act 2014 contains specific requirements on the passing of a resolution in relation to the acquisition by a company of its own shares. It should be consulted before the acquisition proceeds.</p>
                <p>The special resolution will be ineffective if a member holding shares affected by the resolution exercises his / her voting rights in respect of the shares in favour of the resolution and it would not have been carried without his / her vote</p>
